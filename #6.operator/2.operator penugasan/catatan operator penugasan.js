// Operator penugasan adalah operator yang digunakan untuk memberikan tugas kepada variabel.
//  Biasanya digunakan untuk mengisi variabel

// contoh_______
var a = 19;
// Variabel a kita berikan tugas untuk menyimpan nilai 19

// Operator penugasan sama seperti operator aritmatika.
// Ia juga digunakan untuk melakukan operasi aritmatika

// contoh_______
var jumlahView = 12;
// menggunakan operator penugasan penjumlahan
// untuk menambah nilai
jumlahView += 1;

// Variabel jumlahView akan bertambah satu.
// Maksud dari jumlahView += 1 adalah seperti ini :
jumlahView = jumlahView + 1;

// Khusus untuk operator penugasan yang dijumlahkan dan dikurangi dengan satu,
//  bisa disingkat dengan ++ dan -- untuk pengurangan
var a = 2;
a++;

// Operator aritmatika hanya melakukan operasi aritmatika saja,
